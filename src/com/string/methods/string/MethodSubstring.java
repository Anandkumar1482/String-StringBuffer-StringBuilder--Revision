package com.string.methods.string;

/*return substring from begin index to end-1 of the String*/
public class MethodSubstring {
	public static void main(String[] args) {
		String s = "anand kumar";
		System.out.println(s.substring(2)); //begin index to end of the string
		System.out.println(s.substring(2, 8));//begin index to end-1 index
	}
}
